#include "libft.h"

char	*ft_strncat(char *s2, const char *s1, size_t n)
{
	size_t a;
	size_t b;

	a = 0;
	b = 0;
	while (s2[a] != '\0')
		a++;
	while (s1[b] != '\0' && b != n)
	{
		s2[a + b] = s1[b];
		b++;
	}
	s2[a + b] = '\0';
	return (s2);
}
