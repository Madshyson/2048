#include "libft.h"

char	*ft_strcat(char *s2, const char *s1)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (s2[b] != '\0')
		b++;
	while (s1[a] != '\0')
	{
		s2[b + a] = s1[a];
		a++;
	}
	s2[b + a] = '\0';
	return (s2);
}
